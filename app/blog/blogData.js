export function getStoredBlogPosts(){
  if(typeof window==='undefined') return [];
  try{
    const raw=window.localStorage.getItem('ravindu_blog_posts');
    return raw?JSON.parse(raw):[];
  }catch(error){
    console.error('Blog storage read failed:',error);
    return [];
  }
}

export function saveStoredBlogPosts(posts){
  if(typeof window==='undefined') return;
  window.localStorage.setItem('ravindu_blog_posts', JSON.stringify(posts));
}

export function getAllBlogPosts(){
  const customPosts=getStoredBlogPosts();
  return [...customPosts,...defaultBlogPosts]
    .filter(p=>p.published!==false)
    .sort((a,b)=>new Date(b.date||0)-new Date(a.date||0));
}

export function createSlug(title){
  return title.toLowerCase().trim().replace(/[^a-z0-9]+/g,'-').replace(/^-+|-+$/g,'') || `blog-${Date.now()}`;
}
